-- SP_LOGIN     
CREATE PROCEDURE SP_LOGIN_USUARIO
	@CORREO NVARCHAR(55),
    @CONTRASENA NVARCHAR(128),
    @IDRETURN INT OUTPUT,
    @ERRORID INT OUTPUT,
    @ERRORDESCRIPCION NVARCHAR(MAX) OUTPUT
AS
BEGIN
    SET NOCOUNT ON;
    
    DECLARE @ID_PERSONA INT;
    DECLARE @ID_USUARIO INT;

    -- Validate input parameters
    IF @CORREO IS NULL OR @CONTRASENA IS NULL
    BEGIN
        SET @IDRETURN = -1;
        SET @ERRORID = 50001;
        SET @ERRORDESCRIPCION = 'Correo y contraseńa son requeridos.';
        RETURN;
    END

    -- Find the persona by email
    SELECT 
        @ID_PERSONA = P.ID_PERSONA, 
        @ID_USUARIO = U.ID_USUARIO
    FROM TB_PERSONAS P
    INNER JOIN TB_USUARIOS U ON P.ID_PERSONA = U.ID_PERSONA
    WHERE P.CORREO = @CORREO 
      AND U.CONTRASENA = @CONTRASENA;

    -- Check if user exists
    IF @ID_PERSONA IS NULL
    BEGIN
        SET @IDRETURN = -1;
        SET @ERRORID = 50002;
        SET @ERRORDESCRIPCION = 'Correo o contraseńa incorrectos.';
        RETURN;
    END

    -- Return user details for LINQ
        SELECT 
            U.ID_USUARIO AS ID_USUARIO,
            P.ID_PERSONA AS ID_PERSONA,
            P.NOMBRE AS NOMBRE,
            P.CORREO AS CORREO,
            P.TELEFONO AS TELEFONO,
            P.UBICACION AS UBICACION,
            U.ROL AS ROL,
            U.ESTADO AS ESTADO
        FROM TB_USUARIOS U
        INNER JOIN TB_PERSONAS P ON U.ID_PERSONA = P.ID_PERSONA
        WHERE U.ID_PERSONA = @ID_PERSONA

    -- Set success outputs
    SET @IDRETURN = @ID_USUARIO;
    SET @ERRORID = 0;
    SET @ERRORDESCRIPCION = 'Inicio de sesión exitoso.';
END;
GO

