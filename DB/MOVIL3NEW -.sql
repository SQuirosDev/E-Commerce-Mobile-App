CREATE PROCEDURE ACTUALIZAR_CONTRASENA
@CORREO NVARCHAR(55),          -- Correo del usuario
    @CONTRASENA NVARCHAR(128),     -- Nueva contraseńa
    @SEMILLA_1 NVARCHAR(128),      -- Nueva semilla 1
    @SEMILLA_2 NVARCHAR(128),      -- Nueva semilla 2
    @IDRETURN INT OUTPUT,          -- Parámetro de salida para el ID del usuario
    @ERRORID INT OUTPUT,           -- Parámetro de salida para el código de error
    @ERRORDESCRIPCION NVARCHAR(MAX) OUTPUT -- Parámetro de salida para la descripción del error
AS
BEGIN
    SET NOCOUNT ON;
    BEGIN TRY
        -- Verificar si el correo existe en TB_PERSONAS
        IF NOT EXISTS (SELECT 1 FROM TB_PERSONAS WHERE CORREO = @CORREO)
        BEGIN
            -- Si el correo no existe, establecer los valores de salida para indicar el error
            SET @IDRETURN = -1; -- Indica que el correo no está registrado
            SET @ERRORID = 50002; -- Código de error personalizado
            SET @ERRORDESCRIPCION = 'El correo no está registrado.';
            RETURN;
        END;

        -- Obtener el ID_USUARIO asociado al correo
        DECLARE @ID_USUARIO INT;
        SELECT @ID_USUARIO = U.ID_USUARIO
        FROM TB_USUARIOS U
        INNER JOIN TB_PERSONAS P ON U.ID_PERSONA = P.ID_PERSONA
        WHERE P.CORREO = @CORREO;

        -- Actualizar la contraseńa y las semillas en TB_USUARIOS
        UPDATE U
        SET 
            U.CONTRASENA = @CONTRASENA,
            U.SEMILLA_1 = @SEMILLA_1,
            U.SEMILLA_2 = @SEMILLA_2
        FROM TB_USUARIOS U
        INNER JOIN TB_PERSONAS P ON U.ID_PERSONA = P.ID_PERSONA
        WHERE P.CORREO = @CORREO;

        -- Establecer los valores de salida para indicar éxito
        SET @IDRETURN = @ID_USUARIO; -- Devuelve el ID del usuario actualizado
        SET @ERRORID = 0; -- Éxito
        SET @ERRORDESCRIPCION = 'Contraseńa actualizada exitosamente.';
    END TRY
    BEGIN CATCH
        -- Manejar errores y establecer los valores de salida para indicar el error
        SET @IDRETURN = -1; -- Indica que ocurrió un error
        SET @ERRORID = ERROR_NUMBER(); -- Código de error de SQL Server
        SET @ERRORDESCRIPCION = ERROR_MESSAGE(); -- Mensaje de error de SQL Server
    END CATCH
END;
GO
-- SP_INSERTAR_PRODUCTO
CREATE PROCEDURE SP_INSERTAR_PRODUCTO
   @ID_USUARIO INT,
    @NOMBRE NVARCHAR(100),
    @DESCRIPCION NVARCHAR(500),
    @PRECIO DECIMAL(10,2),
    @PRECIO_ALQUILER DECIMAL(10,2) = NULL,
    @TIPO_OPERACION CHAR(1),
    @STOCK INT,
    @CATEGORIA NVARCHAR(50),
    @IMAGEN NVARCHAR(255) = NULL,
    @IDRETURN INT OUTPUT,
    @ERRORID INT OUTPUT,
    @ERRORDESCRIPCION NVARCHAR(MAX) OUTPUT
AS
BEGIN
    -- Validar el tipo de operación
    IF @TIPO_OPERACION NOT IN ('V', 'A', 'B')
    BEGIN
        SET @IDRETURN = -1; -- Indica que el tipo de operación no es válido.
        SET @ERRORID = 50006; -- Código de error personalizado.
        SET @ERRORDESCRIPCION = 'El tipo de operación no es válido. Debe ser "V", "A" o "B".';
        RETURN;
    END;

    -- Insertar el producto
    INSERT INTO TB_PRODUCTOS (
        ID_USUARIO, NOMBRE, DESCRIPCION, PRECIO, PRECIO_ALQUILER, TIPO_OPERACION, STOCK, CATEGORIA, IMAGEN
    )
    VALUES (
        @ID_USUARIO, @NOMBRE, @DESCRIPCION, @PRECIO, @PRECIO_ALQUILER, @TIPO_OPERACION, @STOCK, @CATEGORIA, @IMAGEN
