CREATE PROCEDURE SP_OBTENER_USUARIO
  @ID_USUARIO INT = NULL,
    @IDRETURN INT OUTPUT,
    @ERRORID INT OUTPUT,
    @ERRORDESCRIPCION NVARCHAR(MAX) OUTPUT
AS
BEGIN
    SET NOCOUNT ON;

    -- If no ID is provided, return all active users
    IF @ID_USUARIO IS NULL
    BEGIN
        SELECT 
            U.ID_USUARIO AS IdUsuario,
            P.ID_PERSONA AS IdPersona,
            P.NOMBRE AS NOMBRE,
            P.CORREO AS CORREO,
            P.TELEFONO AS TELEFONO,
            U.ROL AS ROL,
            U.ESTADO AS ESTADO
        FROM TB_USUARIOS U
        INNER JOIN TB_PERSONAS P ON U.ID_PERSONA = P.ID_PERSONA
        WHERE U.ESTADO = 1;
    END
    ELSE
    BEGIN
        -- Buscar usuario específico
        SELECT 
            U.ID_USUARIO AS IdUsuario,
            P.ID_PERSONA AS IdPersona,
            P.NOMBRE AS NOMBRE,
            P.CORREO AS CORREO,
            P.TELEFONO AS TELEFONO,
            P.UBICACION AS UBICACION,
            U.ID_USUARIO AS ID_USUARIO,
            U.ROL AS ROL,
            U.ESTADO AS ESTADO,
            U.FECHA_ULTIMA_COMPRA AS FechaUltimaCompra
        FROM TB_USUARIOS U
        INNER JOIN TB_PERSONAS P ON U.ID_PERSONA = P.ID_PERSONA
        WHERE U.ID_USUARIO = @ID_USUARIO AND U.ESTADO = 1;
    END
END
