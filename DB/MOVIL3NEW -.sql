--SP_REGISTRAR_USUARIO
CREATE PROCEDURE SP_REGISTRAR_USUARIO
    @NOMBRE NVARCHAR(50),
    @CORREO NVARCHAR(55),
    @TELEFONO NVARCHAR(15) = NULL,
    @ROL NVARCHAR(20),
    @CONTRASENA NVARCHAR(128),
    @SEMILLA_1 NVARCHAR(128) = NULL,
    @SEMILLA_2 NVARCHAR(128) = NULL,
    @ESTADO BIT = 1, 
    @ID_USUARIO INT OUTPUT,
    @ERRORID INT OUTPUT,
    @ERRORDESCRIPCION NVARCHAR(MAX) OUTPUT
AS
BEGIN
    BEGIN TRANSACTION;

    BEGIN TRY
        -- Verificar si el correo ya existe
        IF EXISTS (SELECT 1 FROM TB_PERSONAS WHERE CORREO = @CORREO)
        BEGIN
            SET @ERRORID = 1;
            SET @ERRORDESCRIPCION = 'El correo ya está registrado.';
            ROLLBACK TRANSACTION;
            RETURN;
        END

        -- Insertar en TB_PERSONAS
        INSERT INTO TB_PERSONAS (NOMBRE, CORREO, TELEFONO)
        VALUES (@NOMBRE, @CORREO, @TELEFONO);

        SET @ID_USUARIO = SCOPE_IDENTITY();

        -- Insertar en TB_USUARIOS
        INSERT INTO TB_USUARIOS (ID_PERSONA, ROL, CONTRASENA, SEMILLA_1, SEMILLA_2, ESTADO)
        VALUES (@ID_USUARIO, @ROL, @CONTRASENA, @SEMILLA_1, @SEMILLA_2, @ESTADO);

        SET @ID_USUARIO = SCOPE_IDENTITY();

        -- Éxito
        SET @ERRORID = 0;
        SET @ERRORDESCRIPCION = 'Registro exitoso.';

        COMMIT TRANSACTION;
    END TRY
    BEGIN CATCH
        SET @ERRORID = ERROR_NUMBER();
        SET @ERRORDESCRIPCION = ERROR_MESSAGE();
        ROLLBACK TRANSACTION;
    END CATCH;
END;
GO

