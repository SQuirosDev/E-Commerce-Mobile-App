CREATE TRIGGER TRG_ACTUALIZAR_FECHA_COMPRA
ON TB_PRODUCTOS
AFTER UPDATE
AS
BEGIN
    SET NOCOUNT ON;
    
    IF UPDATE(DISPONIBLE)
    BEGIN
        UPDATE U
        SET FECHA_ULTIMA_COMPRA = GETDATE()
        FROM TB_USUARIOS U
        INNER JOIN INSERTED I ON U.ID_USUARIO = I.ID_USUARIO
        WHERE I.DISPONIBLE = 0;
    END
END;
GO

-- TRG_VALIDAR_STOCK
CREATE TRIGGER TRG_VALIDAR_STOCK
ON TB_PRODUCTOS
AFTER UPDATE
AS
BEGIN
    SET NOCOUNT ON;
    
    IF UPDATE(STOCK)
    BEGIN
        UPDATE P
        SET DISPONIBLE = 
            CASE 
                WHEN I.STOCK > 0 THEN 1
                ELSE 0
            END
        FROM TB_PRODUCTOS P
        INNER JOIN INSERTED I ON P.ID_PRODUCTO = I.ID_PRODUCTO;
    END
END;
